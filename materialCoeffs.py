def plotInterpolateCoeffs():
  numPts = 100
  v = np.linspace(0, 1, numPts) # vol frac
  # Cmatrix
  components = ['00', '11']
  C = {}
  for c in components:
    C[c] = np.zeros((numPts))
    for pw in range(6): # order (5) + 1
      C[c] += microStrs['fishscale'][c][str(pw)]*(v**pw)
  plt.figure()
  clrs = ['red', 'black', 'blue', 'cyan', 'pink', 'brown', 'yellow', 'gray', 'green', 'orange'],
  #squircle $ fishscale $ fishscale_half $square $traingle $circle $ellipse $plus $mucosa10 $mucosa20
  mrkrs = ['o', 's', 'd', 'h', '*']
  stls = [':', '--', '-', '-.', '-']
  for ctr, c in enumerate(components):
    plt.plot(v, C[c], color = clrs[ctr], linestyle = stls[ctr], marker = mrkrs[ctr], markevery=20, label = f'$C{c}$')
  plt.xlabel('v')
  plt.ylabel('C')
  plt.legend()
  plt.show()
  
